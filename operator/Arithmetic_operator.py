#这个案例为python算术运算符的操作
a = 21
b = 10
c = 0

c = a + b   #加法#
print("1、c的值为", c)

c = a - b   #减法#
print("2、c的值为", c)

c = a * b   #乘法#
print("3、c的值为", c)

c = a / b  #除法#
print("4、c的值为", c)

c = a % b   #取余#
print("5、c的值为",c)

#修改变量a、b、c
a = 2
b = 3
c = a ** b     #求幂#
print("6、c的值为",c)

a = 10
b = 5
c = a // b      #取整除 - 返回商的整数部分#
print("7、c的值为",c)